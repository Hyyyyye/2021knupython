# Convention, 파이썬 PEP -8
# 1. 한단어로된 변수는 소문자로 적는다.
# 2. 두 단어 이상의 변수는 언더바로 구성한다.
# 3. 예약어와 충돌하는 경우에는 뒤에 언더바를 붙인다.
# 4. 상수의 경우 대문자와 언더바를 혼용하여 쓴다.
